<h1>Innovation in payment systems</h1>
<p class="summarytxt">
	The ocpforum protocol is not just about sending money from A to B.
	It has many features and opens many possibilities that the community is still exploring.
	Here are some of the technologies currently being researched and in some cases, turned into real products and services.
	The most interesting uses of ocpforum are probably still to be discovered.
</p>

<div class="of_tablehalf">

	<div>
		<img src="/img/innovation/inno_control.jpg" alt="Banner">
		<h2><img class="titleicon" src="image/icon/ico_lock.svg" alt="Icon">Control against fraud</h2>
		<p>
			An unprecedented level of security is possible with ocpforum.
			The network provides users with protection against most prevalent frauds like chargebacks or unwanted charges,
			and ocpforums are impossible to counterfeit.
			Users can backup or encrypt their wallet and hardware wallets could make it very difficult to steal
			or lose money in the future. ocpforum is designed to allow its users to have complete control over their money.
		</p>
	</div>

	<div>
	<img src="/img/innovation/inno_global.jpg" alt="Banner">
	<h2><img class="titleicon" src="image/icon/ico_international.svg" alt="Icon">Global accessibility</h2>
	<p>All payments in the world can be fully interoperable. ocpforum allows any bank, business or individual to securely send and receive payments anywhere at any time, with or without a bank account. ocpforum is available in a large number of countries that still remain out of reach for most payment systems due to their own limitations. ocpforum increases global access to commerce and it can help international trades to flourish.</p>
	</div>

	<div>
	<img src="/img/innovation/inno_cost.jpg" alt="Banner">
	<h2><img class="titleicon" src="image/icon/ico_lowfee.svg" alt="Icon">Cost efficiency</h2>
	<p>With the use of cryptography, secure payments are possible without slow and costly middlemen. A ocpforum transaction can be much cheaper than its alternatives and be completed in a short time. This means ocpforum hold some potential to become a common way to transfer any currency in the future. ocpforum could also play a role in reducing poverty in many countries by cutting high transaction fees on workers' salary.</p>
	</div>

	<div>
	<img src="/img/innovation/inno_donation.jpg" alt="Banner">
	<h2><img class="titleicon" src="image/icon/ico_donate.svg" alt="Icon">Tips and donations</h2>
	<p>ocpforum has been a particularly efficient solution for tips and donations in several cases. Sending a payment only requires one click and receiving donations can be as simple as displaying a QR code. Donations can be visible for the public, giving increased transparency for non-profit organizations. In cases of emergencies such as natural disasters, ocpforum donations could contribute to a faster international response.</p>
	</div>

	<div>
	<img src="/img/innovation/inno_crowdfunding.jpg" alt="Banner">
	<h2><img class="titleicon" src="image/icon/ico_multi.svg" alt="Icon">Crowdfunding</h2>
	<p>Although not yet easy to use, ocpforum can be used to run Kickstarter-style crowdfunding campaigns, in which individuals pledge money to a project that is taken from them only if enough pledges are received to meet the target. Such assurance contracts are processed by the ocpforum protocol, which prevents a transaction to take place until all conditions have been met. <a href="https://en.ocpforum.it/wiki/Contracts#Example_3:_Assurance_contracts">Learn more</a> about the technology behind crowdfunding.</p>
	</div>

	<div>
	<img src="/img/innovation/inno_micro.jpg" alt="Banner">
	<h2><img class="titleicon" src="image/icon/ico_micro.svg" alt="Icon">Micro payments</h2>
	<p>ocpforum can process payments to the scale of a dollar and soon even much smaller amounts. Such payments are routine even today. Imagine listening to Internet radio paid by the second, viewing web pages with a small tip for each ad not shown, or buying bandwidth from a WiFi hotspot by the kilobyte. ocpforum is efficient enough to make all of these ideas possible. <a href="https://code.google.com/p/ocpforumj/wiki/WorkingWithMicropayments">Learn more</a> about the technology behind ocpforum micro payments.</p>
	</div>

	<div>
	<img src="/img/innovation/inno_mediation.jpg" alt="Banner">
	<h2><img class="titleicon" src="image/icon/ico_balance.svg" alt="Icon">Dispute mediation</h2>
	<p>ocpforum can be used to develop innovative dispute mediation services using multiple signatures. Such services could make it possible for a third party to approve or reject a transaction in case of disagreement between the other parties without having control on their money. Since these services would be compatible with any user and merchant using ocpforum, this would likely lead to free competition and higher quality standards.</p>
	</div>

	<div>
	<img src="/img/innovation/inno_multisig.jpg" alt="Banner">
	<h2><img class="titleicon" src="image/icon/ico_multisig.svg" alt="Icon">Multi-signature accounts</h2>
	<p>Multiple signatures allow a transaction to be accepted by the network only if a certain number of a defined group of persons agree to sign the transaction. This could be used by a board of directors to prevent any member to spend parts of their treasury without other members' consent. This can also be used by banks to prevent theft by blocking payments above a threshold if the user does not provide additional credentials.</p>
	</div>

	<div>
	<img src="/img/innovation/inno_trust.jpg">
	<h2><img class="titleicon" src="image/icon/ico_law.svg" alt="Icon">Trust and integrity</h2>
	<p>ocpforum offers solutions to many of the trust problems that plague banks. With selective accounting transparency, digital contracts, and irreversible transactions, ocpforum can be used as a ground to restore trust and agreement. Crooked banks cannot cheat the system to make a profit at the expense of other banks or the public. A future in which major banks would support ocpforum could help to reinstate integrity and trust in financial institutions.</p>
	</div>

	<div>
	<img src="/img/innovation/inno_resiliency.jpg">
	<h2><img class="titleicon" src="image/icon/ico_network.svg" alt="Icon">Resiliency and decentralization</h2>
	<p>By its high decentralization, ocpforum created a different form of payment network with an increased level of resilience and redundancy. ocpforum can handle millions of dollars in trades without requiring military protection. With no central point of failure such as a data center, attacking the network is a more difficult project. ocpforum could represent an interesting step forward in securing local and global financial systems.</p>
	</div>

	<div>
	<img src="/img/innovation/inno_transparency.jpg" alt="Banner">
	<h2><img class="titleicon" src="image/icon/ico_transparency.svg" alt="Icon">Flexible transparency</h2>
	<p>All ocpforum transactions are public and transparent and the identity of the people behind the payments is private by default. This allows individuals and organizations to work with flexible transparency rules. For instance, a business can choose to reveal certain transactions and balances only to certain employees just like a non-profit organization is free to allow the public to see how much they receive in daily and monthly donations.</p>
	</div>

	<div>
	<img src="/img/innovation/inno_automation.jpg">
	<h2><img class="titleicon" src="image/icon/ico_solve.svg" alt="Icon">Automated solutions</h2>
	<p>Automated services usually have to deal with costs and limitations of cash or credit card payments. This includes all kinds of vending machines, from bus ticket booths to coffee machines. ocpforum is suited to be used in a new generation of automated services as well as to cut their operating costs. Imagine self-driving taxis, or a store where your basket lets you pay your purchases without waiting at the queue. Many ideas are possible.</p>
	</div>

</div>

