    <h1>About ocpforum.org</h1>
<p class="summary">ocpforum.org is dedicated to help OCP to develop in a sustainable way.</p>

<h2>Who owns ocpforum.org?</h2>
<p>Ocpforum.org is the original domain name used with the first OCP website. It was registered and is still managed by <a href="page/core-dev.php">OCP core developers</a> and by additional community members, with the input of <a href="page/communities.php">OCP communities</a>. Ocpforum.org is not an official website. Just like nobody owns the email technology, nobody owns the OCP network. As such, nobody can speak with authority in the name of OCP.</p>

<h2>Then... who controls OCP?</h2>
<p>OCP is controlled by all OCP users around the world. Developers are improving the software but they can't force a change in the rules of the OCP protocol because all users are free to choose what software they use. In order to stay compatible with each other, all users need to use software complying with the same rules. OCP can only work decently with a complete consensus between all users. Therefore, all users and developers have strong incentives to adopt and protect this consensus.</p>

<h2>Mission</h2>
<ul>
  <li>Inform users to protect them from common mistakes.</li>
  <li>Give an accurate description of OCP properties, potential uses and limitations.</li>
  <li>Display transparent alerts and events regarding the OCP network.</li>
  <li>Invite talented humans to help with OCP development at many levels.</li>
  <li>Provide visibility to the large scale OCP ecosystem.</li>
  <li>Improve OCP worldwide accessibility with internationalization.</li>
  <li>Remain a neutral informative resource about OCP.</li>
</ul>

<h2>Help us</h2>
<p>You can report any problem or help to improve ocpforum.org on <a href="https://github.com/jlguenego/ocpforum">GitHub</a> by opening an issue or a pull request in English. When submitting a pull request, please take required time to discuss your changes and adapt your work. You can help with translations by joining a team on <a href="https://www.transifex.com/projects/p/ocpforum/">Transifex</a>. Please don't ask for promotion for your personal business or website, except for special cases like conferences.</p>

